public class Operaciones {
    double resultado;
    
    public void Suma(double n1, double n2){
        resultado = n1+n2;
    }
    public void Resta(double n1, double n2){
        resultado = n1-n2;
    }
    public void Multiplicacion(double n1, double n2){
        resultado = n1*n2;
    }
    public void Division(double n1, double n2){
        resultado = n1/n2;
    }
}
